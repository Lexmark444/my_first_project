cars = ['bmw', 'audi', 'toyota', 'subaru']
# .sort() makes it alphabetically sorted and .sort(reverse=True) makes it reverse alphabettically sorted
cars.sort()
print(cars)
cars.sort(reverse=True)
print(cars)
print(sorted(cars))
print(cars)
cars.reverse()
print(cars)

# to determine length of list len(cars)
print(len(cars))


